import numpy as np
import matplotlib.pyplot as plt

# Спеціальна магічна команда для вбудовування графіків безпосередньо у Colab
# %matplotlib inline

# Налаштування естетики графіків для презентаційних цілей
plt.rcParams['figure.figsize'] = (14, 7)
plt.rcParams['font.size'] = 12
plt.rcParams['axes.grid'] = True
plt.rcParams['grid.linestyle'] = '--'
plt.rcParams['grid.alpha'] = 0.6

def simulate_udai_river_pollution():
    """
    Функція здійснює чисельне розв'язання рівняння транспорту забруднень
    у річці методом скінченних різниць (Upwind-схема) та візуалізує результат.
    """

    # --------------------------------------------------------------------------
    # 1. ГІДРОЛОГІЧНІ ТА ФІЗИКО-ХІМІЧНІ ПАРАМЕТРИ (Річка Удай)
    # --------------------------------------------------------------------------
    L = 20000.0        # Загальна довжина модельованої ділянки русла, метри (20 км)
    T = 36000.0        # Загальний фізичний час симуляції, секунди (10 годин)

    u = 0.2            # Середня швидкість течії річки (адвекція), м/с
    D = 10.0           # Коефіцієнт поздовжньої турбулентної дисперсії, м²/с
    k_day = 0.35       # Константа швидкості розпаду БСК (1/доба)
    k = k_day / 86400.0 # Переведення константи розпаду у систему СІ (1/секунда)

    # --------------------------------------------------------------------------
    # 2. ПАРАМЕТРИ ЧИСЕЛЬНОЇ ДИСКРЕТИЗАЦІЇ (Генерація розрахункової сітки)
    # --------------------------------------------------------------------------
    nx = 1000          # Кількість просторових вузлів (розбиваємо 20 км на 1000 частин)
    dx = L / nx        # Просторовий крок сітки, м (в даному випадку 20 метрів)

    # Автоматичний розрахунок безпечного кроку за часом (dt) для гарантування
    # математичної стійкості різницевої схеми.
    # Враховуємо обидва критерії: умову Куранта (CFL) для адвекції та умову Фур'є для дифузії.
    # Коефіцієнт 0.8 використовується як запас міцності (Safety factor).
    dt_advection = dx / u
    dt_diffusion = (dx**2) / (2 * D)
    dt = min(dt_advection, dt_diffusion) * 0.8

    nt = int(T / dt)   # Загальна кількість розрахункових часових кроків

    # Виведення службової інформації для контролю
    print(f"=== Ініціалізація моделі ===")
    print(f"Крок по простору dx = {dx:.2f} м")
    print(f"Крок по часу dt = {dt:.2f} с. Кількість кроків nt = {nt}")

    # Розрахунок безрозмірних чисел, що входять у різницеве рівняння
    Cr = u * dt / dx         # Число Куранта
    alpha = D * dt / (dx**2) # Дифузійне число
    decay = k * dt           # Фактор розпаду на одному кроці часу

    print(f"Число Куранта (Cr) = {Cr:.4f} (повинно бути < 1 для стійкості)")

    # --------------------------------------------------------------------------
    # 3. ПОЧАТКОВІ ТА ГРАНИЧНІ УМОВИ (t = 0)
    # --------------------------------------------------------------------------
    # Створюємо масив координат x для кожного вузла
    x = np.linspace(0, L, nx)

    # Масиви для зберігання концентрацій на поточному (C) та наступному (C_next) кроці
    C = np.zeros(nx)
    C_next = np.zeros(nx)

    # Моделюємо аварійний скид органіки. Задаємо початковий розподіл як криву Гауса.
    x0 = 2000.0        # Точка локалізації скиду (2 км від початку ділянки)
    sigma = 200.0      # Дисперсія початкової плями (характеризує ширину розтікання)
    C_max = 100.0      # Максимальна стартова концентрація (наприклад, БСК₅ у мг/л)

    # Розрахунок початкового профілю концентрацій
    C = C_max * np.exp(-((x - x0)**2) / (2 * sigma**2))
    C_initial = np.copy(C) # Зберігаємо для фінального графіку

    # Словник для збереження проміжних результатів (зрізи кожні 2 години)
    save_interval_seconds = 7200 # 2 години в секундах
    save_interval_steps = int(save_interval_seconds / dt)
    results = {0: C_initial}

    # --------------------------------------------------------------------------
    # 4. ГОЛОВНИЙ ЦИКЛ ІНТЕГРУВАННЯ ЗА ЧАСОМ (Симуляція)
    # --------------------------------------------------------------------------
    # Використовуємо векторизовані операції NumPy (зрізи масивів) замість повільних
    # циклів 'for', що прискорює розрахунки в Python у сотні разів.
    for n in range(1, nt + 1):

        # Обчислення концентрації для всіх внутрішніх просторових вузлів (від 1 до nx-2).
        # C[1:-1] - поточний вузол i
        # C[0:-2] - лівий вузол i-1 (вгору за течією)
        # C[2:]   - правий вузол i+1 (вниз за течією)

        C_next[1:-1] = C[1:-1] \
                       - Cr * (C[1:-1] - C[0:-2]) \
                       + alpha * (C[2:] - 2*C[1:-1] + C[0:-2]) \
                       - decay * C[1:-1]

        # Граничні умови (Boundary Conditions)
        C_next = C   # Умова Діріхле на вході: постійний фоновий нульовий притік
        C_next[-1] = C[-2] # М'яка умова Неймана (вільний вихід) на нижній межі розрахункової області

        # Оновлення робочого масиву концентрацій для наступного витка циклу
        C = np.copy(C_next)

        # Збереження "зрізів" (snapshot) концентрацій кожні 2 години для візуалізації
        if n % save_interval_steps == 0 or n == nt:
            current_time_hours = (n * dt) / 3600
            results[current_time_hours] = np.copy(C)

    # --------------------------------------------------------------------------
    # 5. ВІЗУАЛІЗАЦІЯ РЕЗУЛЬТАТІВ МОДЕЛЮВАННЯ (Побудова графіків)
    # --------------------------------------------------------------------------
    plt.figure()

    # Побудова початкового стану (t=0)
    plt.plot(x / 1000, C_initial, 'k--', label='t = 0 год (Момент аварійного скиду)', linewidth=2.5)
    plt.fill_between(x / 1000, 0, C_initial, color='gray', alpha=0.15)

    # Кольорова палітра для відображення динаміки в часі
    colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728', '#9467bd', '#8c564b']
    color_idx = 0

    # Побудова графіків для кожного збереженого часового зрізу
    for t_hours, profile in results.items():
        if t_hours > 0:
            plt.plot(x / 1000, profile, color=colors[color_idx % len(colors)],
                     label=f't = {t_hours:.1f} год', linewidth=2)
            color_idx += 1

    # Оформлення графіку
    plt.title('Просторово-часова динаміка органічного забруднення в річці Удай\n(Модель 1D ADRE: Адвекція, Дифузія, Розпад БСК)',
              fontsize=16, fontweight='bold', pad=15)
    plt.xlabel('Відстань вниз за течією від витоку (км)', fontsize=14)
    plt.ylabel('Концентрація органічного забруднювача (умовні од., мг/л)', fontsize=14)

    # Додавання інформаційного боксу з параметрами симуляції
    param_text = (f"Фізичні параметри:\n"
                  f"Швидкість течії (u) = {u} м/с\n"
                  f"Дисперсія (D) = {D} м²/с\n"
                  f"Коефіцієнт розпаду (k) = {k_day} добу⁻¹")

    plt.text(14, 80, param_text, fontsize=12,
             bbox=dict(facecolor='white', alpha=0.9, edgecolor='#cccccc', boxstyle='round,pad=0.5'))

    plt.legend(loc='upper right', fontsize=12, shadow=True, fancybox=True)
    plt.tight_layout()
    plt.show()

# Запуск головної функції
if __name__ == "__main__":
    simulate_udai_river_pollution()