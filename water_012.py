import jax
import jax.numpy as jnp
from jax.scipy.sparse.linalg import gmres
import numpy as np
import time
import matplotlib.pyplot as plt

# ==============================================================================
# 1. ІНІЦІАЛІЗАЦІЯ
# ==============================================================================
jax.config.update("jax_enable_x64", True)

try:
    DEVICE = jax.devices()[0] # Беремо перший доступний пристрій
    print(f"JAX backend: {DEVICE.platform.upper()}")
except Exception:
    print("Пристрої не знайдено, використовується CPU.")

# Параметри сітки
NX, NY = 500, 200
L_X, L_Y = 1000.0, 100.0
dx, dy = L_X / NX, L_Y / NY
N_TOT = NX * NY

# Фізичні параметри
U = 0.5        # Швидкість (м/с)
DL = 10.0      # Поздовжня дисперсія
DT = 0.5       # Поперечна дисперсія (трохи збільшив для наочності)
K_EFF = 5e-5   # Розпад

# ==============================================================================
# 2. КОЕФІЦІЄНТИ FDM (Схема Upwind)
# ==============================================================================
# Рівняння: U * dC/dx = DL * d2C/dx2 + DT * d2C/dy2 - K*C + S
# Дискретизація призводить до системи A*C = B.
# Ми переносимо все, що залежить від C, вліво (в матрицю A).
# A_P * C_P - (A_W * C_W + A_E * C_E + ...) = S

# Коефіцієнти впливу сусідів (додатні величини)
AW = DL / dx**2 + U / dx  # West (Upwind advection + diffusion)
AE = DL / dx**2           # East (Diffusion only)
AS = DT / dy**2           # South
AN = DT / dy**2           # North

# Центральний коефіцієнт (сума витоків + реакція)
# Знак: Матриця зазвичай формується як (Diag - OffDiag).
# Тут ми робимо Diag позитивним, а сусідів віднімаємо в operator.
AP = (2*DL/dx**2 + 2*DT/dy**2 + U/dx + K_EFF)

print(f"dx: {dx:.2f}, dy: {dy:.2f}")
print(f"Коефіцієнти: AW={AW:.2f}, AE={AE:.2f}, AP={AP:.2f}")

# ==============================================================================
# 3. MATVEC ОПЕРАТОР (Без циклічних артефактів)
# ==============================================================================

@jax.jit
def matvec_operator(C_1D):
    """
    Обчислює ліву частину рівняння A * C.
    Реалізує 'No-flux' (Neumann) на стінках Y і 'Outflow' на X=L.
    X=0 (Вхід) не оновлюється тут, бо це умова Діріхле (фіксується в правій частині).
    """
    C = C_1D.reshape((NY, NX))

    # 1. Головна діагональ
    res = AP * C

    # 2. Сусіди по X (West / East)
    # Замість roll використовуємо зсув з заповненням нулями, щоб уникнути циклічності
    # C_{i-1, j}
    C_West = jnp.pad(C, ((0, 0), (1, 0)), mode='constant')[:, :-1]
    # C_{i+1, j}
    C_East = jnp.pad(C, ((0, 0), (0, 1)), mode='constant')[:, 1:]

    res -= AW * C_West
    res -= AE * C_East

    # 3. Сусіди по Y (South / North)
    # C_{i, j-1}
    C_South = jnp.pad(C, ((1, 0), (0, 0)), mode='edge')[:, :] # 'edge' імітує Neumann dC/dy=0
    C_South = C_South[:-1, :] # обрізаємо зайве

    # C_{i, j+1}
    C_North = jnp.pad(C, ((0, 1), (0, 0)), mode='edge')[:, :]
    C_North = C_North[1:, :]

    # Увага: Для Neumann (стінки) ми часто просто не віднімаємо потік назовні,
    # або додаємо його назад до центру. Тут спрощений підхід:
    # Вздовж Y використовуємо стандартну різницю, але 'edge' паддінг
    # робить C_{boundary-1} = C_{boundary}, що дає нульовий потік.

    # Але для строгості матричного множення з фіксованими коефіцієнтами:
    # Краще використовувати нульовий паддінг і коригувати діагональ,
    # але для простоти залишимо нульовий паддінг (Dirichlet 0 на стінках за замовчуванням)
    # або просто 'constant' 0, якщо стінки поглинають.
    # Для річки береги ізольовані (Neumann). Реалізуємо це явно:

    C_S_shifted = jnp.pad(C, ((1, 0), (0, 0)))[:-1, :]
    C_N_shifted = jnp.pad(C, ((0, 1), (0, 0)))[1:, :]

    # На берегах (Y=0, Y=NY-1) потік через стінку відсутній.
    # Це означає, що ми не віднімаємо AS * C_neighbor, якщо сусіда немає.
    # Але в матричному вигляді простіше залишити 0 за межами.

    res -= AS * C_S_shifted
    res -= AN * C_N_shifted

    return res.ravel()

# ==============================================================================
# 4. ПРАВА ЧАСТИНА (B) ТА ДЖЕРЕЛА
# ==============================================================================

C_IN = 1.0  # Концентрація на вході
S_TERM = jnp.zeros((NY, NX))

# Параметри джерела
SOURCE_VAL = 1.0  # Інтенсивність
X_S, X_E = int(200/dx), int(400/dx)
Y_S, Y_E = int(30/dy), int(70/dy) # Джерело по центру річки, а не на всю ширину

# !!! ВИПРАВЛЕННЯ: Встановлюємо значення тільки в зоні джерела
S_TERM = S_TERM.at[Y_S:Y_E, X_S:X_E].set(SOURCE_VAL)

# Формуємо вектор B (джерело)
B = S_TERM

# Гранична умова Діріхле на вході (X=0)
# В рівнянні для вузла i=1 (перший внутрішній):
# ... - AW * C_0 + AP * C_1 ... = S
# Переносимо відомий C_0 в праву частину: B_1 = S + AW * C_IN
B = B.at[:, 0].add(AW * C_IN) # Додаємо "втік" забруднення з входу

B_1D = B.ravel()

# ==============================================================================
# 5. РОЗВ'ЯЗАННЯ (GMRES)
# ==============================================================================

print("\nЗапуск GMRES...")
start = time.time()

# Pre-compile
_ = matvec_operator(jnp.zeros(N_TOT)).block_until_ready()

solution, info = gmres(
    lambda x: matvec_operator(x),
    B_1D,
    tol=1e-5,
    maxiter=2000,
    restart=100
)
solution.block_until_ready()
end = time.time()

print(f"Час: {end - start:.4f} с. Info: {info}")

# ==============================================================================
# 6. ВІЗУАЛІЗАЦІЯ
# ==============================================================================

C_FINAL = solution.reshape((NY, NX))

# Встановлюємо візуально вхідну границю (бо вирішувач не оновлює "віртуальний" стовпець -1)
# Фактично C_FINAL[:, 0] це перший розрахунковий вузол.

x = np.linspace(0, L_X, NX)
y = np.linspace(0, L_Y, NY)
X_mesh, Y_mesh = np.meshgrid(x, y)

plt.figure(figsize=(10, 4))
# !!! ВИПРАВЛЕННЯ: Не використовуємо .T для Z, бо X_mesh має форму (NY, NX) як і C_FINAL
cnt = plt.contourf(X_mesh, Y_mesh, np.array(C_FINAL), levels=100, cmap='viridis')
plt.colorbar(cnt, label='Концентрація')
plt.title(f'Поширення азоту (JAX/{DEVICE.platform.upper()})')
plt.xlabel('Довжина (м)')
plt.ylabel('Ширина (м)')

# Рамка джерела
plt.plot([X_S*dx, X_E*dx, X_E*dx, X_S*dx, X_S*dx],
         [Y_S*dy, Y_S*dy, Y_E*dy, Y_E*dy, Y_S*dy], 'r--', label='Джерело')
plt.legend()
plt.tight_layout()
plt.show()

print(f"Макс. концентрація: {jnp.max(C_FINAL):.4f}")