import numpy as np
import matplotlib.pyplot as plt

# =======================================================
# 1. КОНСТАНТИ ТА ПАРАМЕТРИ МОДЕЛІ (на основі джерел)
# =======================================================

# ----------------- Гідравлічні параметри -----------------
L = 80000.0  # Довжина сегмента річки для моделювання (м). Використовуємо 80 км для демонстрації.
V = 0.5  # Середня швидкість течії (м/с). Типове значення для П. Бугу: 0.2 – 0.6 м/с [19].
DL = 20.0  # Коефіцієнт поздовжньої дисперсії (м²/с). Типове значення для руслових ділянок: 10–50 м²/с [20].
R_NH4 = 2.0  # Ретардаційний фактор для амонію (R > 1, враховує сорбцію) [3].
H = 2.0  # Середня глибина річки (м). Типове значення: 1.5 – 3.0 м [21].

# ----------------- Кінетичні параметри (ставки при 20°C) -----------------
T = 20.0 # Температура води (°C)
Theta = 1.08 # Температурний коефіцієнт [22].
DO = 5.0 # Концентрація розчиненого кисню (мг/дм³).

# k1: NH4+ -> NO2- (константа нітрифікації I фази, за добу)
k_N1_20 = 0.05 / 86400.0  # Переводимо в с⁻¹ (типове значення 0.01 – 0.05 д⁻¹ [22]).
k_N2_20 = 0.05 / 86400.0  # NO2- -> NO3- (константа нітрифікації II фази)

# kD: NO3- денітрифікація (ареальна ставка)
k_D_A_20 = 0.005 / 86400.0  # Ареальна ставка денітрифікації (мг/м²/с).
K_O2 = 1.0  # Константа напівнасичення киснем (лімітування)

# ----------------- Чисельні параметри -----------------
Nx = 800  # Кількість просторових вузлів (сегментів).
dx = L / Nx  # Крок по простору (м).
T_sim = 48 * 3600.0  # Загальний час симуляції (48 годин у секундах)
dt = 100.0  # Крок по часу (с). Перевірка CFL: dt <= 100/(0.5) = 200 с; dt <= 10000/(40) = 250 с. (Використовуємо dt=100 для запасу стійкості).
Nt = int(T_sim / dt)  # Кількість кроків по часу.

# =======================================================
# 2. ПЕРЕДПІДГОТОВКА ТА ІНІЦІАЛІЗАЦІЯ
# =======================================================

# Розрахунок кінетичних констант з корекцією на температуру
def get_k_temp_corrected(k_20, T, Theta):
    return k_20 * (Theta ** (T - 20.0))

k_N1_T = get_k_temp_corrected(k_N1_20, T, Theta)
k_N2_T = get_k_temp_corrected(k_N2_20, T, Theta)

# Об'ємна ставка денітрифікації (враховує глибину H)
k_D_V_T = get_k_temp_corrected(k_D_A_20, T, Theta) / H
# Припускаємо, що сорбція R_Sor і асиміляція R_Alg постійні або нульові для спрощення
k_Sor = 0.0  # Сорбція амонію (с⁻¹)
k_Alg = 0.0  # Асиміляція нітратів (с⁻¹)

# Початкові умови (концентрація N-сполук уздовж річки)
x_coords = np.linspace(0, L, Nx)
C_NH4 = np.zeros(Nx)
C_NO2 = np.zeros(Nx)
C_NO3 = np.zeros(Nx)

# Точкове джерело забруднення (наприклад, нижче Хмельницького, x=0)
C_source_NH4 = 6.0  # Максимальна концентрація амонію (мг/дм³)
source_node = 0

# =======================================================
# 3. МОДЕЛЬНІ ФУНКЦІЇ (КІНЕТИКА)
# =======================================================

# Функція лімітування киснем (f(DO))
def f_DO(DO, K_O2):
    if DO > 0:
        return DO / (K_O2 + DO)
    return 0.0

# Функція розрахунку реакційних членів (для кожного вузла)
def calculate_reaction_rates(C_nh4, C_no2, C_no3, DO_factor):
    # R_N1: NH4+ -> NO2- (споживає NH4, продукує NO2)
    R_N1 = k_N1_T * C_nh4 * DO_factor

    # R_N2: NO2- -> NO3- (споживає NO2, продукує NO3)
    R_N2 = k_N2_T * C_no2 * DO_factor

    # R_D: NO3- -> N2 (споживає NO3)
    R_D = k_D_V_T * C_no3

    # R_Sor (Спрощено, споживає NH4)
    R_Sor = k_Sor * C_nh4

    # R_Alg (Спрощено, споживає NO3)
    R_Alg = k_Alg * C_no3

    return R_N1, R_N2, R_D, R_Sor, R_Alg

# =======================================================
# 4. СИМУЛЯЦІЙНИЙ ЦИКЛ
# =======================================================

results_nh4 = []
results_no3 = []
C_temp_nh4 = C_NH4.copy()
C_temp_no2 = C_NO2.copy()
C_temp_no3 = C_NO3.copy()

DO_factor = f_DO(DO, K_O2)

print(f"Симуляція розпочата: {int(T_sim / 3600)} годин...")

# Цикл по часу (явна схема FTCS)
for t_step in range(Nt):

    # 1. Встановлення граничної умови (точковий скид NH4+ на x=0)
    C_NH4[source_node] = C_source_NH4

    # Використовуємо явну схему для Адвекції та Дисперсії (FTCS)
    for i in range(1, Nx - 1):

        # --- Транспортні члени (Адвекція + Дисперсія) ---

        # Дифузія (D)
        D_term_nh4 = DL * (C_NH4[i+1] - 2 * C_NH4[i] + C_NH4[i-1]) / (dx**2)
        D_term_no2 = DL * (C_NO2[i+1] - 2 * C_NO2[i] + C_NO2[i-1]) / (dx**2)
        D_term_no3 = DL * (C_NO3[i+1] - 2 * C_NO3[i] + C_NO3[i-1]) / (dx**2)

        # Адвекція (A)
        A_term_nh4 = -V * (C_NH4[i] - C_NH4[i-1]) / dx
        A_term_no2 = -V * (C_NO2[i] - C_NO2[i-1]) / dx
        A_term_no3 = -V * (C_NO3[i] - C_NO3[i-1]) / dx

        # --- Реакційні члени (R) ---
        R_N1, R_N2, R_D, R_Sor, R_Alg = calculate_reaction_rates(C_NH4[i], C_NO2[i], C_NO3[i], DO_factor)

        # --- Оновлення концентрацій ---

        # Рівняння (1) для NH4
        Net_R_NH4 = -R_N1 - R_Sor
        dC_dt_nh4 = D_term_nh4 + A_term_nh4 + Net_R_NH4
        C_temp_nh4[i] = C_NH4[i] + dt * (dC_dt_nh4 / R_NH4) # Ділимо на R_NH4 [3]

        # Рівняння (2) для NO2
        Net_R_NO2 = R_N1 - R_N2
        dC_dt_no2 = D_term_no2 + A_term_no2 + Net_R_NO2
        C_temp_no2[i] = C_NO2[i] + dt * dC_dt_no2

        # Рівняння (3) для NO3
        Net_R_NO3 = R_N2 - R_D - R_Alg
        dC_dt_no3 = D_term_no3 + A_term_no3 + Net_R_NO3
        C_temp_no3[i] = C_NO3[i] + dt * dC_dt_no3

    C_NH4 = C_temp_nh4.copy()
    C_NO2 = C_temp_no2.copy()
    C_NO3 = C_temp_no3.copy()

    # Збереження результатів для побудови графіків через певні інтервали
    if t_step % (Nt // 5) == 0 or t_step == Nt - 1:
        results_nh4.append(C_NH4.copy())
        results_no3.append(C_NO3.copy())

# =======================================================
# 5. ВІЗУАЛІЗАЦІЯ РЕЗУЛЬТАТІВ
# =======================================================

plt.figure(figsize=(14, 6))

# Графік для NH4+
plt.subplot(1, 2, 1)
for i, C in enumerate(results_nh4):
    time_hr = (i * (Nt // 5) * dt) / 3600
    if i == Nt // 5 * 5 // (Nt // 5):
         time_hr = T_sim / 3600 # Остаточний час

    plt.plot(x_coords / 1000, C, label=f'Час: {time_hr:.1f} год')

plt.title(f'Динаміка Амонійного Азоту ($C_{{NH4}}$) вздовж р. Південний Буг (L={L/1000:.0f} км)')
plt.xlabel('Відстань від скиду, км')
plt.ylabel('Концентрація, мг/дм³')
plt.legend()
plt.grid(True)
plt.ylim(0, C_source_NH4 * 1.1)


# Графік для NO3-
plt.subplot(1, 2, 2)
for i, C in enumerate(results_no3):
    time_hr = (i * (Nt // 5) * dt) / 3600
    if i == Nt // 5 * 5 // (Nt // 5):
         time_hr = T_sim / 3600

    plt.plot(x_coords / 1000, C, label=f'Час: {time_hr:.1f} год')

plt.title(f'Динаміка Нітратного Азоту ($C_{{NO3}}$) (Продукт Нітрифікації)')
plt.xlabel('Відстань від скиду, км')
plt.ylabel('Концентрація, мг/дм³')
plt.legend()
plt.grid(True)

plt.tight_layout()
plt.show()

print(f"Примітка: Максимальна концентрація NH4+ у ст. №1 (Копистин) фіксувалася до 33.8 ГДК [7].")
print(f"Змодельована початкова концентрація NH4+ у скиді: {C_source_NH4} мг/дм³.")